import pdb

import skimage
from skimage import io


def ratio_bbox_to_xyxy(bbox, H, W):
    x1, y1, x2, y2 = bbox
    x1 = int(x1 * W)
    y1 = int(y1 * H)
    x2 = int(x2 * W)
    y2 = int(y2 * H)
    return [x1, y1, x2, y2]


LINE_WIDTH = 5


def draw_ratio_bbox(im, bboxes):
    for bbox in bboxes:
        x1, y1, x2, y2 = bbox
        for i in range(LINE_WIDTH):
            xx1 = x1 - i
            xx2 = x2 - i
            yy1 = y1 - i
            yy2 = y2 - i
            rr, cc = skimage.draw.rectangle_perimeter(
                start=(yy1, xx1), end=(yy2, xx2), shape=im.shape
            )
            skimage.draw.set_color(im, (rr, cc), (255, 0, 0))


def draw_bulb_centroids(im, bulb_centroids):
    for x, y in bulb_centroids:
        rr, cc = skimage.draw.circle(x, y, 20)
        skimage.draw.set_color(im, (rr, cc), (0, 255, 0))


